           private void setAllTextFields(NhanVien nv) {
       txtId.setText(nv.getId());
        txtName.setText(nv.getTen());
        txtDoB.setText(nv.getNgaysinh());
        txtSDT.setText(nv.getSdt());
        txtEmail.setText(nv.getEmail());
        txtUsername.setText(nv.getAcount().getUsername());
        txtPassword.setText(nv.getAcount().getPassword());
        txtPosition.setText(nv.getVitri());
        txtSalary.setText(String.valueOf(nv.getLuong()));
        //Thiết lập focus vào txtMaSinhVien (con trỏ vào trường này)
        txtId.requestFocus();
    }
