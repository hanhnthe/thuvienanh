    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
         try {
             // TODO add your handling code here:
             dataOut.writeUTF("xemnhanvien");
         } catch (IOException ex) {
             Logger.getLogger(XemThongTinNV.class.getName()).log(Level.SEVERE, null, ex);
         }
        String Tk = txtSearch.getText();
        System.out.println(""+chuanHoa(Tk));
        try {
            dataOut.writeUTF(chuanHoa(Tk));// gui thong tin nhap vao ve server
            result =  (ArrayList<NhanVien>) ios.readObject();
            if(result != null ){
                handleReturnData(result);
            }else{
                JOptionPane.showMessageDialog(message, "Thong tin tim kiem khong ton tai");
            }
        } catch (IOException ex) {
            Logger.getLogger(XemThongTinNV.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(XemThongTinNV.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            if(socket!=null){
                try {
                    socket.close();
                } catch (IOException ex) {
                    Logger.getLogger(XemThongTinNV.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }

        }

        private void handleReturnData(ArrayList<NhanVien> list) {

            DefaultTableModel model = (DefaultTableModel) TableMuon.getModel();//xuat ra tabe
            model.getDataVector().clear();
            if (null != list) {
                for(int j=0; j<list.size(); j++){
                    NhanVien nhanVien = list.get(j);
                    model.addRow(new Object[]{j+1, nhanVien.getId(),nhanVien.getTen()});
                }

            }
    }//GEN-LAST:event_btnSearchActionPerformed
public static String chuanHoa(String s) {
        String x = s.toLowerCase().trim().replaceAll("\\s+", " ");
        return x;
    }
